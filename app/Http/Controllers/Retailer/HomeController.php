    {
        $shops = Auth::user()->shops()->paginate(25);

        if($shops->count() > 1){
            return view('dashboard/retailer/switch-shops', compact('shops'));
        }

        // redirect to that specific shops route
        $my_shop = $shops->first();
        return redirect()->route('retailer.shops.index', ['shop' => $my_shop->slug]);

